package Prototype;

public class Student implements Prototype{
    private int id;
    private String name;

    public Student(int id, String name) {
        this.id = id;
        this.name = name;
    }

    @Override
    public String toString() {
        return "Student{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }

    @Override
    public Prototype clone() {
        return new Student(this.id,(this.name));
    }
}
